package gov.ornl.vvuq.model;

public class Test23Response {

	
	private String name;
	private String type;
	private String total_score;
	private String dissemination_equipment;
	private String postproduction_equipment;
	private String research_words_and_materials;
    private String [] keywords;


    public Test23Response() {

    }


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public String getType() {
		return type;
	}


	public void setType(String type) {
		this.type = type;
	}


	public String getDissemination_equipment() {
		return dissemination_equipment;
	}


	public void setDissemination_equipment(String dissemination_equipment) {
		this.dissemination_equipment = dissemination_equipment;
	}


	public String getPostproduction_equipment() {
		return postproduction_equipment;
	}


	public void setPostproduction_equipment(String postproduction_equipment) {
		this.postproduction_equipment = postproduction_equipment;
	}


	public String getResearch_words_and_materials() {
		return research_words_and_materials;
	}


	public void setResearch_words_and_materials(String research_words_and_materials) {
		this.research_words_and_materials = research_words_and_materials;
	}


	public String [] getKeywords() {
		return keywords;
	}


	public void setKeywords(String [] keywords) {
		this.keywords = keywords;
	}


	public String getTotal_score() {
		return total_score;
	}


	public void setTotal_score(String total_score) {
		this.total_score = total_score;
	}

    

}