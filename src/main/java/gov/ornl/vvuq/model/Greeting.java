package gov.ornl.vvuq.model;



public class Greeting {

    private String id;

    private String text;

    public Greeting() {

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

}